	addu	$t4,$t2,$t3
#         Leer sueldo del elemento i-ésimo
	lw	$t5,40($t4)
#         Acumular suma sobre $s0
	addu	$s0,$s0,$t5
#         Si el índice iguala el valor tope, salir
	bne	$t0,$t1,inc
